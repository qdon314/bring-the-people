'use client'

import React from 'react'
import Link from 'next/link'
import { usePathname } from 'next/navigation'
import { cn } from '@/shared/lib/utils'

interface SidebarProps {
  showId: string
  cycleId: string
}

const navItems = [
  { 
    label: 'Overview', 
    href: (showId: string, cycleId: string) => `/shows/${showId}/cycles/${cycleId}/overview`,
    icon: ChartIcon 
  },
  { 
    label: 'Plan', 
    href: (showId: string, cycleId: string) => `/shows/${showId}/cycles/${cycleId}/plan`,
    icon: ClipboardIcon 
  },
  { 
    label: 'Create', 
    href: (showId: string, cycleId: string) => `/shows/${showId}/cycles/${cycleId}/create`,
    icon: SparklesIcon 
  },
  { 
    label: 'Run', 
    href: (showId: string, cycleId: string) => `/shows/${showId}/cycles/${cycleId}/run`,
    icon: PlayIcon 
  },
  { 
    label: 'Results', 
    href: (showId: string, cycleId: string) => `/shows/${showId}/cycles/${cycleId}/results`,
    icon: ChartBarIcon 
  },
  { 
    label: 'Memo', 
    href: (showId: string, cycleId: string) => `/shows/${showId}/cycles/${cycleId}/memo`,
    icon: DocumentIcon 
  },
]

export function Sidebar({ showId, cycleId }: SidebarProps) {
  const pathname = usePathname() ?? ''

  return (
    <aside className="flex h-full w-60 flex-col border-r border-border bg-surface">
      <nav className="flex flex-1 flex-col gap-1 p-3">
        {navItems.map((item) => {
          const href = item.href(showId, cycleId)
          const isActive = pathname === href || pathname.startsWith(href + '/')

          return (
            <Link
              key={item.label}
              href={href}
              className={cn(
                'nav-item',
                isActive
                  ? 'bg-primary-light text-primary'
                  : 'text-text-muted hover:bg-bg hover:text-text'
              )}
              aria-current={isActive ? 'page' : undefined}
            >
              <item.icon className="h-5 w-5 flex-shrink-0" />
              <span className="hidden lg:inline">{item.label}</span>
            </Link>
          )
        })}
      </nav>
    </aside>
  )
}

